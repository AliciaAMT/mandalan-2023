The best way to learn how to play is to explore the game itself. Exploration is made easy through the use of tables with headings.<br />
Players may simply browse the tables and listen to the first cell heading, which will be a description of the table. Navigation keys vary depending on the screenreader, but usually the 't' button tabs through the tables or Fire Vox can be set up to list headings.<br /><p>Movement is based on a grid of tiles, and the player may move north, south, east, or west (unless there is an obstruction or map border). In graphics mode movement is accomplished by merely clicking on the map tile to which you would like to move, keeping in mind the center tile is always the tile where you are currently located. In text mode movement is done by clicking on the direction you would like to move in the movement table.</p>
